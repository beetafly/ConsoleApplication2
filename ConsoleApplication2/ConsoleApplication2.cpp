#include <iostream>
using namespace std;

int кількість_позитивних_елементів(float масив[], int розмір) {
    int кількість = 0;
    for (int i = 0; i < розмір; ++i) {
        if (масив[i] > 0) {
            кількість++;
        }
    }
    return кількість;
}

float сума_після_останнього_нуля(float масив[], int розмір) {
    int останній_нуль = -1;
    for (int i = розмір - 1; i >= 0; --i) {
        if (масив[i] == 0) {
            останній_нуль = i;
            break;
        }
    }

    if (останній_нуль == -1) {
        return 0;
    }

    float сума = 0;
    for (int i = останній_нуль + 1; i < розмір; ++i) {
        сума += масив[i];
    }
    return сума;
}

int main() {
    const int розмір = 9;
    float масив[розмір] = { 1, -2, 3, 0, 4, 5, 0, -6, 7 };

    cout << "Кількість позитивних елементів у масиві: " << кількість_позитивних_елементів(масив, розмір) << endl;
    cout << "Сума елементів після останнього нуля у масиві: " << сума_після_останнього_нуля(масив, розмір) << endl;

    return 0;
}


